% Gauss消元法
A = [1, 2; -1, 0];
b = [4; 3];
A = [A b];

A(2,:) = -1/A(2,1) * A(1,:) + A(2,:);

% Gauss-Jordan消元法
A = [1, 2; -1, 0];
b = [4; 3];
A = [A b];

A(2,:) = -A(1,1)/A(2,1) * A(1,:) + A(2,:);
A(1,:) = -A(1,2)/A(2,2) * A(2,:) + A(1,:);

% RREF消元法
A = [1, 2; -1, 0];
b = [4; 3];
A = [A b];

A(2,:) = -A(1,1)/A(2,1) * A(1,:) + A(2,:);
A(1,:) = -A(1,2)/A(2,2) * A(2,:) + A(1,:);

A(1,:) = A(1,:) / A(1,1);
A(2,:) = A(2,:) / A(2,2);