help fix % 向0取整：fix(X)将X的元素四舍五入到接近于零的整数。
help floor % 向下取整：取整(X)将X的元素四舍五入到接近负无穷的整数。
help ceil % 向上取整：ceil(X)将X的元素四舍五入到接近无穷大的整数。
help round % 四舍五入：向最接近的整数舍入
disp('-----fix-----')
fix(-1.1)
fix(1.1)
fix(0.4)
fix(0.6)
disp('-----floor-----')
floor(-1.1)
floor(1.1)
floor(0.4)
floor(0.6)
disp('-----ceil-----')
ceil(-1.1)
ceil(1.1)
ceil(0.4)
ceil(0.6)
disp('-----round-----')
round(-1.1)
round(1.1)
round(0.4)
round(0.6)
